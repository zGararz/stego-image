    private void jLabelImageInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelImageInMouseClicked
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Images", "png", "jpg", "jpeg");
        chooser.addChoosableFileFilter(filter);
        int result = chooser.showOpenDialog(this);
        if(result == JFileChooser.APPROVE_OPTION){
            File file = chooser.getSelectedFile();
            imagePath = file.getAbsolutePath();
            jLabelImageIn.setIcon(resizeImage(imagePath, jLabelImageIn));
            jLabelImageOut.setIcon(null);
        }

    }//GEN-LAST:event_jLabelImageInMouseClicked


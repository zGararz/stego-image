    private void jButtonExtractActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExtractActionPerformed
        try {
            if(imagePath.isEmpty() || !jTextFieldKey.getText().matches("[0-9]*")) {
                JOptionPane.showMessageDialog(this, "Error!");
            } else {
            BufferedImage img = ImageIO.read(new File(imagePath));

            String text = HideText.getText(img, Integer.parseInt(jTextFieldKey.getText()));
            
            int l = Integer.parseInt(jTextFieldLength.getText());
            if(l > 0) {
                jTextAreaText.setText(text.substring(0, l));
            } else {
                jTextAreaText.setText(text);
            }

            
            JOptionPane.showMessageDialog(this, "Extracted!");
            }
        } catch (IOException ex) {
            Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error!");
        }
    
    }//GEN-LAST:event_jButtonExtractActionPerformed

    private void jButtonEmbedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEmbedActionPerformed
        try { 
            if(imagePath.isEmpty() || jTextAreaText.getText().isEmpty() || !jTextFieldKey.getText().matches("[0-9]*")) {
                JOptionPane.showMessageDialog(this, "Error!");
            } else {
                BufferedImage img = ImageIO.read(new File(imagePath));
            
            stegoImage = HideText.hideText(img, jTextAreaText.getText(), Integer.parseInt(jTextFieldKey.getText()));
            if (stegoImage == null) {
                JOptionPane.showMessageDialog(this, "Choose another photo!");
                
            } else {
                jLabelImageOut.setIcon(resizeImage(stegoImage, jLabelImageIn));
                JOptionPane.showMessageDialog(this, "Embedded!");
            }
            }
            
            
            
            
        } catch (IOException ex) {
            Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error!");
        }
    }//GEN-LAST:event_jButtonEmbedActionPerformed

